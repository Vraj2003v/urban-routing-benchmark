# ├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг

class UpdateGenerator:

    def __init__(self, cfg, pattern="random", mode="synthetic"):
        self.cfg       = cfg
        self.pattern   = pattern
        self.mode      = mode
        self.magnitude = cfg["experiment"]["update_magnitude_pct"]
        self.edge_pool = []
        self.live_cache = None

        self._load_edge_pool()

        if mode == "live":
            self.live_cache = LiveDataCache(cfg)
            print(f"[UpdateGen] Live mode active ├бРѓгРђЮ "
                  f"{len(self.live_cache.delays)} routes, "
                  f"{len(self.live_cache.incidents)} incidents loaded.")
        else:
            print(f"[UpdateGen] Synthetic mode (pattern={pattern}).")

        self.start_time = time.time()

    def _load_edge_pool(self, pool_size=10000):
        pg = self.cfg["postgres"]
        conn = psycopg2.connect(
            host=pg["host"], port=pg["port"],
            dbname=pg["database"], user=pg["user"], password=pg["password"]
        )
        cur = conn.cursor()
        cur.execute(f"""
            SELECT e.id, e.cost,
                   ST_Y(ST_Centroid(e.geom)) AS lat,
                   ST_X(ST_Centroid(e.geom)) AS lon,
                   e.osm_id
            FROM road_edges e
            WHERE e.cost > 0 AND e.geom IS NOT NULL
            ORDER BY random()
            LIMIT {pool_size}
        """)
        rows = cur.fetchall()
        if not rows:
            cur.execute(f"""
                SELECT e.id, e.cost, n.lat, n.lon, e.osm_id
                FROM road_edges e
                JOIN road_nodes n ON e.source = n.id
                WHERE e.cost > 0
                ORDER BY random()
                LIMIT {pool_size}
            """)
            rows = cur.fetchall()
        self.edge_pool = [(r[0], r[1], r[2] or 0.0, r[3] or 0.0, r[4])
                          for r in rows]
        cur.close()
        conn.close()
        print(f"[UpdateGen] Loaded {len(self.edge_pool)} edges into update pool "
              f"(with lat/lon for proximity matching).")

    def _synthetic_multiplier(self):
        if self.pattern == "sinusoidal":
            elapsed = time.time() - self.start_time
            peak = 0.5 * (1 + math.sin(2 * math.pi * elapsed / 120.0))
            return 1.0 + self.magnitude * peak
        elif self.pattern == "spike":
            if random.random() < 0.05:
                return 1.0 + random.uniform(0.5, 2.0)
            return 1.0 + random.uniform(-self.magnitude * 0.5, self.magnitude * 0.5)
        else:
            return 1.0 + random.uniform(-self.magnitude, self.magnitude)

    def _live_multiplier(self, edge_lat, edge_lon):
        base = 1.0

        incidents = self.live_cache.incidents
        if incidents:
            base *= _incident_multiplier(edge_lat, edge_lon, incidents)

        here = self.live_cache.here_speeds
        if here:
            best_dist, best_speed = float("inf"), None
            for (hlat, hlon), spd in here.items():
                d = _haversine_km(edge_lat, edge_lon, hlat, hlon)
                if d < best_dist:
                    best_dist, best_speed = d, spd
            if best_speed and best_dist < 0.3:
                base *= max(0.2, 50.0 / max(best_speed, 1.0))

        if base == 1.0:
            base = 1.0 + random.uniform(-0.1, 0.1)

        return min(max(base, 0.1), 5.0)

    def generate_batch(self, batch_size=50):
        sample = random.sample(self.edge_pool, min(batch_size, len(self.edge_pool)))
        updates = []
        for edge_id, base_cost, lat, lon, osm_id in sample:
            if self.mode == "live":
                mult = self._live_multiplier(lat, lon)
            else:
                mult = self._synthetic_multiplier()
            new_cost = max(0.1, base_cost * mult * random.uniform(0.95, 1.05))
            updates.append((edge_id, base_cost, round(new_cost, 4)))
        return updates

    def apply_to_postgres(self, conn, updates):
        cur = conn.cursor()
        psycopg2.extras.execute_values(
            cur,
            "UPDATE road_edges SET cost = data.new_cost "
            "FROM (VALUES %s) AS data(edge_id, new_cost) "
            "WHERE road_edges.id = data.edge_id",
            [(u[0], u[2]) for u in updates],
            template="(%s::bigint, %s::float)"
        )
        psycopg2.extras.execute_values(
            cur,
            "INSERT INTO edge_weight_log (edge_id, old_cost, new_cost) VALUES %s",
            [(u[0], u[1], u[2]) for u in updates]
        )
        conn.commit()
        cur.close()

    def apply_to_neo4j(self, session, updates):
        session.run(
            """
            UNWIND $updates AS upd
            MATCH ()-[r:ROAD {edge_id: upd.edge_id}]->()
            SET r.cost = upd.new_cost
            """,
            updates=[{"edge_id": u[0], "new_cost": u[2]} for u in updates]
        )

    def stop(self):
        if self.live_cache:
            self.live_cache.stop()
