# Proximity helpers
# ├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг├бРђЮРѓг

def _haversine_km(lat1, lon1, lat2, lon2):
    R = 6371.0
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = (math.sin(dlat/2)**2
         + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2))
         * math.sin(dlon/2)**2)
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def _incident_multiplier(edge_lat, edge_lon, incidents):
    SEVERITY_WEIGHT = {"Minor": 0.3, "Moderate": 0.6, "Major": 1.5,
                       "Critical": 2.0, "Unknown": 0.2}
    total = 1.0
    for inc in incidents:
        dist_km = _haversine_km(edge_lat, edge_lon, inc["lat"], inc["lon"])
        if dist_km < 0.5:
            w = SEVERITY_WEIGHT.get(inc.get("severity", "Minor"), 0.3)
            decay = math.exp(-dist_km / 0.25)
            total += w * decay
    return min(total, 4.0)


